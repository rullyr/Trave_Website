               <p class="text-justify"> Lampung adalah sebuah provinsi paling selatan di Pulau Sumatra, Indonesia, dengan ibu kota Bandar Lampung.
                   Provinsi ini memiliki dua kota yaitu Kota Bandar Lampung dan Kota Metro serta 13 kabupaten.
                   Posisi Lampung secara geografis berada di sebelah barat berbatasan dengan Samudra Hindia, di sebelah timur dengan Laut Jawa, di sebelah utara berbatasan dengan provinsi Sumatra Selatan, dan di sebelah selatan berbatasan dengan Selat Sunda.
                   Provinsi Lampung lahir pada tanggal 18 Maret 1964 dengan ditetapkannya Peraturan Pemerintah Nomor 3/1964 yang kemudian menjadi Undang-undang Nomor 14 tahun 1964.
                   Sebelum itu Provinsi Lampung merupakan keresidenan yang tergabung dengan Provinsi Sumatra Selatan.
               </p>
               <p class="text-justify"> Nuwo sesat adalah salah satu rumah adat yang ada di Provinsi Lampung.
                   Nuwo Sesat berfungsi sebagai tempat pertemuan adat bagi para purwatin (Penyimbang) pada saat mengadakan pepung adat (Musyawarah).
                   Karena itu rumah adat ini juga disebut Balai Agung.
                   Bagian-bagian dari rumah adat ini adalah Anjungan yang merupakan serambi yang digunakan untuk pertemuan kecil, Pusiban adalah ruang dalam yang digunakan sebagai tempat musyawarah resmi, ruang Tetabuhan adalah ruangan tempat menyimpan alat musik tradisional, ruang Gajah Merem yang digunakan untuk tempat istirahat bagi para penyimbang, dan ijan geladak adalah tangga masuk yang dilengkapi dengan atap.
