               <p class="text-justify"> Rumah Gapura Candi Bentar sejatinya merujuk pada bangunan gapura yang menjadi gerbang rumah-rumah adat Bali.
                   Gapura tersebut terdiri dari dua buah candi yang serupa dan sebangun dan membatasi sisi kiri dan sisi kanan pintu masuk ke pekarangan rumah.
                   Gapura-gapura tersebut tidak memiliki atap penghubung pada bagian atasnya sehingga kedua sisinya terpisah sempurna, dan hanya terhubung di baagian dalam olehk-anak tangga yang menjadi jalan masuk.
                   Gapura Candi Bentar dalam arsitektur Bali merupakan sebuah perwujudan bangunan yang berfungsi untuk masuk-keluar dari satu sisi ke sisi lainnya (dari luar ke dalam dan atau sebaliknya).
                   Pada awalnya ketika arsitektur Bali masih sesuai dengan keadaan pada masa kerajaan, Gapura Candi Bentar hanya dibangun di lingkungan Puri (Istana Raja) dan Pura (tempat suci agama Hindu).
