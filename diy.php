               <p class="text-justify"> Daerah Istimewa Yogyakarta (bahasa Jawa: ꦝꦲꦺꦫꦃ​ꦆꦱ꧀ꦠꦶꦩꦺꦮ​ꦔꦪꦺꦴꦒꦾꦏꦂꦠ, translit. Dhaérah Istiméwa Ngayogyakarta) adalah Daerah Istimewa setingkat provinsi di Indonesia yang merupakan peleburan Negara Kesultanan Yogyakarta dan Negara Kadipaten Paku Alaman.
                   Daerah Istimewa Yogyakarta terletak di bagian selatan Pulau Jawa, dan berbatasan dengan Provinsi Jawa Tengah dan Samudera Hindia.
                   Daerah Istimewa yang memiliki luas 3.185,80 km² ini terdiri atas satu kota, dan empat kabupaten, yang terbagi lagi menjadi 78 kapanewon/kemantren, dan 438 kalurahan/kelurahan.
                   Menurut sensus penduduk 2010 memiliki populasi 3.452.390 jiwa dengan proporsi 1.705.404 laki-laki, dan 1.746.986 perempuan, serta memiliki kepadatan penduduk sebesar 1.084 jiwa per km².
                   Penyebutan nomenklatur Daerah Istimewa Yogyakarta yang terlalu panjang menimbulkan penyingkatan nomenklatur menjadi DI Yogyakarta atau DIY.
                   Daerah Istimewa Yogyakarta sering dihubungkan dengan Kota Yogyakarta sehingga secara kurang tepat sering disebut dengan Jogja, Yogya, Yogyakarta, Jogjakarta.
                   Walau secara geografis merupakan daerah setingkat provinsi terkecil kedua setelah DKI Jakarta.
               </p>
               <p class="text-justify"> Rumah adat dari Daerah Istimewa Yogyakarta (DIY) berbentuk rumah Joglo.
                   Tidak hanya di Yogyakarta, Joglo sebenarnya juga banyak dikembangkan dan diakui sebagai rumah suku Jawa yang bermukim di provinsi Jawa Tengah dan Jawa Timur.
                   Nama Joglo sendiri merupakan akronim dari tajug loro, sebagai hasil stilasi dari bentuk atap meru (tajug) bertumpuk dua (loro) lapis pada peratapan rumah.
                   Di kalangan keraton di Yogyakarta rumah Joglo dikenal dengan sebagai Joglo Keraton yang identik dengan bangunan Bangsal Kencono.
                   Rumah Joglo Yogyakarta baik di kalangan keraton maupun rakyat, memiliki aksen bangunan tradisional Jawa dengan ciri atap berbentuk bubungan tinggi seperti gunungan yang namanya atap meru bertumpuk tiga.
                   Uniknya, walaupun tidak bertipologi rumah panggung, rumah joglo ini sudah dikembangkan dengan konsep anti gempa, mengacu pada geografis Yogyakarta yang berada di lempengan rawan gempa.

