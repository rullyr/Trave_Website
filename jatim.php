               <p class="text-justify"> Jawa Timur (disingkat Jatim, bahasa Jawa: Hanacaraka: ꧋ꦗꦮꦮꦺꦠꦤ꧀, Pegon: جاوا ويتان, translit. Jawa Wétan, bahasa Madura: Jhâbâh Dhimor) adalah sebuah provinsi di bagian timur Pulau Jawa, Indonesia.
                   Ibu kotanya terletak di Surabaya.
                   Luas wilayahnya 47.922 km², dan jumlah penduduknya 39.698.631 jiwa (per 2019).
                   Jawa Timur memiliki wilayah terluas di antara 6 provinsi di Pulau Jawa, dan memiliki jumlah penduduk terbanyak kedua di Indonesia setelah Jawa Barat.
                   Jawa Timur berbatasan dengan Laut Jawa di utara, Selat Bali di timur, Samudra Hindia di selatan, serta Provinsi Jawa Tengah di barat.
                   Wilayah Jawa Timur juga meliputi Pulau Madura, Pulau Bawean, Pulau Kangean serta sejumlah pulau-pulau kecil di Laut Jawa (Kepulauan Masalembu), dan Samudera Hindia (Pulau Sempu, dan Nusa Barung).
                   Provinsi ini didiami oleh kelompok etnis yang beragam, seperti suku Jawa, suku Madura, dan orang Tionghoa-Indonesia.
                   Sebagian besar masyarakat di Jawa Timur menganut agama Islam, sekitar 96% dari total populasi.
                   Agama lain seperti Kristen, yang sebagian besar dianut oleh orang Tionghoa-Indonesia dan imigran dari Indonesia Timur dan Sumatra Utara, dan juga agama Hindu yang kebanyakan dianut oleh suku Tengger di Taman Nasional Bromo Tengger Semeru dan suku Bali yang mendiami bagian paling timur provinsi yang berbatasan dengan Bali. Bahasa Indonesia adalah bahasa resmi provinsi dan juga seluruh bangsa, tetapi bahasa Jawa dan bahasa Madura adalah bahasa yang paling sering digunakan.
                   Bahasa Indonesia hanya digunakan untuk komunikasi antar-etnis dan keperluan resmi.
               </p>
               <p class="text-justify"> Joglo Situbondo adalah rumah adat provinsi Jawa Timur.
                   Meskipun memiliki nama Joglo, namun rumah adat ini berbeda dengan rumah tradisional Joglo di Jawa Tengah.
                   Tak dipungkiri juga keduanya memiliki beberapa kemiripan.
                   Kegunaan rumah ini adalah sebagai rumah tempat tinggal, namun ada juga yang memanfaatkannya sebagai peninggalan bersejarah.
                   Hal ini dikarenakan rumah ini kental dengan budaya nenek moyang di masa lalu.
                   Sinkretisme agama dan kepercayaan kejawen memberikan pengaruh terhadap bentuk dan tata ruang rumah adat Joglo.
                   Rumah Joglo Situbondo ini memiliki ciri khas dengan kesederhanaannya, namun memiliki cita rasa seni yang tinggi.
                   Secara ukuran, rumah adat ini tidak terlalu besar.
                   Rumah tradisional ini memiliki bentuk limasan atau dara gepak.
                   Joglo Situbondo sesuai dengan namanya banyak ditemukan di daerah Situbondo, Jawa Timur.
                   Hal inilah yang menjadikan namanya menjadi Joglo Situbondo.
                   Namun, selain di sana, rumah adat ini juga banyak ditemukan di Ponorogo.
