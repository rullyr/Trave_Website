               <p class="text-justify"> Jawa Tengah (disingkat Jateng, Hanacaraka: ꦗꦮ​ꦠꦼꦔꦃ; Jawa: Jåwå Tengah, Pegon: جاوا تٓڠه) adalah sebuah provinsi Indonesia yang terletak di bagian tengah Pulau Jawa.
                   Ibu kotanya adalah Semarang.
                   Provinsi ini berbatasan dengan Provinsi Jawa Barat di sebelah barat, Samudra Hindia dan Daerah Istimewa Yogyakarta di sebelah selatan, Jawa Timur di sebelah timur, dan Laut Jawa di sebelah utara.
                   Luas wilayahnya 32.800,69 km², atau sekitar 28,94% dari luas pulau Jawa.
                   Provinsi Jawa Tengah juga meliputi Pulau Nusakambangan di sebelah selatan (dekat dengan perbatasan Jawa Barat), serta Kepulauan Karimun Jawa di Laut Jawa.
                   Penduduk Jawa Tengah berdasarkan Badan Pusat Statistik tahun 2020 berjumlah 34.718.204 jiwa dengan kepadatan 1.058,46 jiwa/km².
               </p>
               <p class="text-justify"> Rumah adat Jolgo merupakan rumah adat Jawa Tengah.
                   Nama rumah adat Joglo ini terdiri dari dua kata yang digabung menjadi satu.
                   Yaitu kata “Tajug” dan “Loro” yang memiliki arti menggabungkan dua Tajug.
                   Sedangkan untuk Tajug sendiri merupakan bentuk atap seperti piramid.
                   Masyarakat jawa saat itu memilih Tajug sebagai model atap rumah ini, karena bentuknya yang hampir sama dengan gunung.
                   Jaman dulu, gunung dianggap sebagai tempat yang sakral oleh masyarakat yang hidup kala itu.
                   Empat tiang pada atap rumah ini juga memiliki filosofi yaitu gambaran kekuatan dari empat penjuru mata angin.
                   Dimana memberi keyakinan bahwa berlindung di rumah dengan atap soko guru mampu menghindari terjadinya bencana.
                   Tak hanya itu jenis atap ini, juga sebagai tanda bukti terimakasih dan pujian kepada sang guru.
