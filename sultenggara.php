               <p class="text-justify"> Sulawesi Tenggara (singkatan: Sultra) adalah sebuah provinsi di pulau Sulawesi yang membentuk semenanjung tenggara pulau tersebut, bersama dengan sejumlah pulau lepas pantai yang besar seperti Buton, Muna, Kabaena dan Wawonii (dahulu bernama Wowoni ), bersama dengan pulau-pulau kecil.
                   Ibukotanya adalah Kendari, di pantai timur semenanjung.
                   Provinsi Sulawesi Tenggara terletak di Jazirah Tenggara Pulau Sulawesi, secara geografis terletak di bagian selatan garis khatulistiwa di antara 02°45' – 06°15' Lintang Selatan dan 120°45' – 124°30' Bujur Timur serta mempunyai wilayah daratan seluas 38.140 km² (3.814.000 ha) dan perairan (laut) seluas 110.000 km² (11.000.000 ha).
                   Provinsi ini tidak memiliki jalan raya yang menghubungkan ke seluruh pulau, dan jalur transportasi utama adalah feri yang melintasi Teluk Bone antara Watampone (Bone) di Sulawesi Selatan dan pelabuhan Kolaka di Sulawesi Tenggara.
               </p>
               <p class="text-justify"> Rumah adat Buton atau Buton merupakan bangunan di atas tiang, dan seluruhnya dari bahan kayu.
                   Bangunannya terdiri dari empat tingkat atau empat lantai.
                   Ruang lantai pertama lebih luas dari lantai kedua.
                   Sedangkan lantai keempat lebih besar dari lantai ketiga, jadi makin ke atas makin kecil atau sempit ruangannya, tetapi di lantai keempat sedikit lebih melebar.
                   Seluruh bangunan tanpa memakai paku dalam pembuatannya, melainkan memakai pasak atau paku kayu.
                   Tiang-tiang depan terdiri dari 5 buah yang berjajar ke belakang sampai delapan deret, hingga jumlah seluruhnya adalah 40 buah tiang.
                   Tiang tengah menjulang ke atas dan merupakan tiang utama disebut Tutumbu yang artinya tumbuh terus.
                   Tiang-tiang ini terbuat dari kayu wala dan semuanya bersegi empat.
                   Untuk rumah rakyat biasa, tiangnya berbentuk bulat.
                   Biasanya tiang-tiang ini puncaknya terpotong.
                   Dengan melihat jumlah tiang sampingnya dapat diketahui siapa atau apa kedudukan si pemilik.
                   Rumah adat yang mempunyai tiang samping 4 buah berarti rumah tersebut terdiri dari 3 petak merupakan rumah rakyat biasa.
                   Rumah adat bertiang samping 6 buah akan mempunyai 5 petak atau ruangan, rumah ini biasanya dimiliki oleh pegawai Sultan atau rumah anggota adat kesultanan Buton.
                   Sedangkan rumah adat yang mempunyai tiang samping 8 buah berarti rumah tersebut mempunyai 7 ruangan dan ini khusus untuk rumah Sultan Buton.
